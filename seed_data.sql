INSERT INTO course_contents (course_id, section_id, title, content_type, resource_url, file_reference, description, uploaded_by)
SELECT
    cs.course_id,
    cs.section_id,
    CONCAT(cs.title, ' Overview'),
    'slide',
    CONCAT('https://school.edu/content/course-', cs.course_id, '/section-', cs.section_id),
    NULL,
    'Autogenerated lecture slides',
    cl.lecturer_id
FROM content_sections cs
JOIN course_lecturers cl ON cl.course_id = cs.course_id;

INSERT INTO assignments (course_id, title, description, due_datetime, max_score, created_by)
SELECT
    c.course_id,
    CONCAT('Assignment 1 - ', c.course_code),
    'Autogenerated assignment for testing',
    TIMESTAMP('2026-06-01 23:59:00') + INTERVAL (c.course_id - 1) DAY,
    100,
    cl.lecturer_id
FROM courses c
JOIN course_lecturers cl ON cl.course_id = c.course_id;

INSERT INTO assignment_submissions (assignment_id, student_id, submission_text, submission_url)
SELECT
    a.assignment_id,
    ce.student_id,
    CONCAT('Submission by student ', ce.student_id, ' for assignment ', a.assignment_id),
    CONCAT('https://school.edu/submissions/', a.assignment_id, '/', ce.student_id)
FROM assignments a
JOIN course_enrollments ce ON ce.course_id = a.course_id
WHERE ce.student_id IN (
    SELECT user_id
    FROM users
    WHERE role_id = 3
      AND user_id <= (
          SELECT MIN(user_id) + 9999
          FROM users
          WHERE role_id = 3
      )
);

INSERT INTO assignment_grades (submission_id, graded_by, score, feedback)
SELECT
    s.submission_id,
    cl.lecturer_id,
    60 + MOD(s.submission_id, 41),
    'Autogenerated feedback'
FROM assignment_submissions s
JOIN assignments a ON a.assignment_id = s.assignment_id
JOIN course_lecturers cl ON cl.course_id = a.course_id;
