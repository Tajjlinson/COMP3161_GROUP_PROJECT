INSERT INTO users (user_code, full_name, email, password_hash, role_id)
SELECT
    CONCAT('lect', LPAD(seq_num, 3, '0')),
    CONCAT('Lecturer ', seq_num),
    CONCAT('lect', LPAD(seq_num, 3, '0'), '@school.edu'),
    'lecturer123',
    2
