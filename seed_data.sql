INSERT INTO courses (course_code, course_name, description, created_by)
SELECT
    CONCAT('COMP', LPAD(seq_num, 4, '0')),
    CONCAT('Course ', seq_num),
    CONCAT('Autogenerated course description for course ', seq_num),
    1
FROM (
    SELECT
        ROW_NUMBER() OVER (ORDER BY a.d, b.d, c.d) AS seq_num
    FROM digits a
    CROSS JOIN digits b
    CROSS JOIN digits c
    LIMIT 200
) numbered_courses;

INSERT INTO course_lecturers (course_id, lecturer_id)
SELECT
    c.course_id,
    lecturer_user.user_id
FROM (
    SELECT
        course_id,
        ROW_NUMBER() OVER (ORDER BY course_id) AS course_position
    FROM courses
) c
JOIN (
    SELECT
        user_id,
        ROW_NUMBER() OVER (ORDER BY user_id) AS lecturer_position
    FROM users
    WHERE role_id = 2
) lecturer_user
    ON lecturer_user.lecturer_position = FLOOR((c.course_position - 1) / 5) + 1;

